package org.ovirt.engine.sdk4.internal.containers;

import org.ovirt.engine.sdk4.types.Application;
import org.ovirt.engine.sdk4.types.Vm;

public class ApplicationContainer extends IdentifiedContainer implements Application {
    private Vm vm;
    
    public Vm vm() {
        return vm;
    }
    
    public void vm(Vm newVm) {
        vm = newVm;
    }
    
    public boolean vmPresent() {
        return vm != null;
    }
    
}
